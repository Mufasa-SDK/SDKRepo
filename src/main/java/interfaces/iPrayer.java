package interfaces;

/**
 * Interface defining interactions with prayer abilities in a game.
 * This interface includes methods for activating different prayers and checking if they are currently active.
 */
public interface iPrayer {

    // Activate ones
    void activateAugury();
    void activateBurstofStrength();
    void activateChivalry();
    void activateClarityofThought();
    void activateEagleEye();
    void activateHawkEye();
    void activateImprovedReflexes();
    void activateIncredibleReflexes();
    void activateMysticLore();
    void activateMysticMight();
    void activateMysticWill();
    void activatePiety();
    void activatePreserve();
    void activateProtectfromMagic();
    void activateProtectfromMelee();
    void activateProtectfromMissiles();
    void activateProtectItem();
    void activateRapidHeal();
    void activateRapidRestore();
    void activateRedemption();
    void activateRetribution();
    void activateRigour();
    void activateRockSkin();
    void activateSharpEye();
    void activateSmite();
    void activateSteelSkin();
    void activateSuperhumanStrength();
    void activateThickSkin();
    void activateUltimateStrength();

    // isActive checks
    boolean isActiveAugury();
    boolean isActiveBurstofStrength();
    boolean isActiveChivalry();
    boolean isActiveClarityofThought();
    boolean isActiveEagleEye();
    boolean isActiveHawkEye();
    boolean isActiveImprovedReflexes();
    boolean isActiveIncredibleReflexes();
    boolean isActiveMysticLore();
    boolean isActiveMysticMight();
    boolean isActiveMysticWill();
    boolean isActivePiety();
    boolean isActivePreserve();
    boolean isActiveProtectfromMagic();
    boolean isActiveProtectfromMelee();
    boolean isActiveProtectfromMissiles();
    boolean isActiveProtectItem();
    boolean isActiveRapidHeal();
    boolean isActiveRapidRestore();
    boolean isActiveRedemption();
    boolean isActiveRetribution();
    boolean isActiveRigour();
    boolean isActiveRockSkin();
    boolean isActiveSharpEye();
    boolean isActiveSmite();
    boolean isActiveSteelSkin();
    boolean isActiveSuperhumanStrength();
    boolean isActiveThickSkin();
    boolean isActiveUltimateStrength();
}
